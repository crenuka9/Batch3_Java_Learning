package day4;

public class Encapsulation
{
    private int a= 10;
    private int b= 30;
    private int c= 50;

    public int getA()
    {
        return a;
    }
    public void setA(int a)
    {
        this.a=a;
    }
    public int getB()
    {
        return b;
    }
    public void setB(int b)
    {
        this.b=b;
    }
    public int getC()
    {
        return c;
    }
    public void setC(int c)
    {
        this.c=c;
    }
}

